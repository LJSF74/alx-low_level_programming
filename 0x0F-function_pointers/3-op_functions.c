#include "3-calc.h"
/**
 * op_add - calcs the sum of two ints
 * @a: first int
 * @b: secnond int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts b from a
 * @a: first int
 * @b: secnond int
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a by b
 * @a: first int
 * @b: secnond int
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides a by b
 * @a: first int
 * @b: secnond int
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder of a divided by b
 * @a: first int
 * @b: secnond int
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
